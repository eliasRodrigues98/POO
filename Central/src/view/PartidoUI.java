    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        
        if (jTextNome.getText().trim().equals("") || jTextNumero.getText().trim().equals("") 
                || jTextSigla.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos.", "Alerta", 
                    JOptionPane.WARNING_MESSAGE);
        } else {
            String nome = jTextNome.getText();
            System.out.println(nome);
            int numero = Integer.parseInt(jTextNumero.getText());
            System.out.println(numero);
            String sigla = jTextSigla.getText();
            System.out.println(sigla);
            boolean ok = c.cadastrarPartidos(nome, numero, sigla);
            System.out.println(ok);
            if (ok == true) {
                JOptionPane.showMessageDialog(null, "Cadastro realizado com sucesso.", 
                        "Sucesso", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "Erro durante o cadastro.", 
                        "Alerta", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

